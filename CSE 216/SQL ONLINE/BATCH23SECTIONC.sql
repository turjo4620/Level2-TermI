SELECT 
EMPLOYEE_ID,
FIRST_NAME, 
LAST_NAME,
(SUBSTR(FIRST_NAME, 1, 1) || SUBSTR(LAST_NAME, 1, 1)) AS TAG
FROM EMPLOYEES
WHERE (SUBSTR(FIRST_NAME, 1, 1) = SUBSTR(LAST_NAME, 1, 1))
ORDER BY EMPLOYEE_ID


