SELECT MANAGER_ID,
SUM(SALARY)
FROM EMPLOYEES
GROUP BY MANAGER_ID
HAVING SUM(SALARY) > 30000 AND (COUNT(*)) > 5
ORDER BY  SUM(SALARY) DESC

