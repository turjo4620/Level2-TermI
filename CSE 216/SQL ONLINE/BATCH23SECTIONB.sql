SELECT
DEPARTMENT_ID,
TO_CHAR(SUM(SALARY * 12), '999,999,999') AS total_payout
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(*) > 2 AND MIN(SALARY) >= 2500


