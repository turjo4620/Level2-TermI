SELECT EMPLOYEE_ID,
SUBSTR(UPPER(FIRST_NAME), 1, 2) || LENGTH(LAST_NAME) || SUBSTR(PHONE_NUMBER, -1)  "Security Code"
FROM EMPLOYEES
WHERE LENGTH(FIRST_NAME) > LENGTH(LAST_NAME)
ORDER BY LENGTH(SUBSTR(UPPER(FIRST_NAME), 1, 2) || LENGTH(LAST_NAME) || SUBSTR(PHONE_NUMBER, -1)) DESC




