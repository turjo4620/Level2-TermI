SELECT 
JOB_ID,
(MAX(SALARY) - MIN(SALARY)) SALARY_GAP
FROM EMPLOYEES
GROUP BY JOB_ID
HAVING (MAX(SALARY) - MIN(SALARY)) > 3000 AND (MAX(SALARY) + MIN(SALARY)) / 2 > 6000


















