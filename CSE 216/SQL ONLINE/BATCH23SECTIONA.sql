SELECT 
EMPLOYEE_ID, 
(FIRST_NAME || ' ' || LAST_NAME) FullName,
(LOWER(SUBSTR(FIRST_NAME, 1, 3)) || SUBSTR(PHONE_NUMBER, -3)) USER_ID
FROM EMPLOYEES
WHERE LENGTH(PHONE_NUMBER)  >= 10 AND DEPARTMENT_ID IN (50, 80)
ORDER BY LENGTH(FIRST_NAME) DESC

