HIRE_DATE
FROM EMPLOYEES
WHERE SUBSTR(LOWER(FIRST_NAME), 1, 1) NOT IN('a', 'e', 'i', 'o', 'u')
AND LOWER(LAST_NAME) NOT LIKE '%b%' AND TRIM(TO_CHAR(HIRE_DATE, 'MM')) = '11'
