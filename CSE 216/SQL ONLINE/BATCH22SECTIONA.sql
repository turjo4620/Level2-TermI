SELECT 

  'Q' || CEIL(EXTRACT(MONTH FROM HIRE_DATE)/3) AS QUARTER,
  COUNT(*) AS EMPLOYEE_COUNT
  
FROM EMPLOYEES
GROUP BY 'Q' || CEIL(EXTRACT(MONTH FROM HIRE_DATE)/3)
ORDER BY 'Q' || CEIL(EXTRACT(MONTH FROM HIRE_DATE)/3);










